    public class SectionsPagerAdapter extends FragmentPagerAdapter {
        private final FragmentManager mFragmentManager;

        public SectionsPagerAdapter(FragmentManager fm) {
            super(fm);
            mFragmentManager = fm;
        }

        @Override
        public Fragment getItem(int position) {
            // getItem is called to instantiate the fragment for the given page.
            // Return a PlaceholderFragment (defined as a static inner class below).
            //return MainActivityOld.PlaceholderFragment.newInstance(position + 1);
            Log.i(TAG, "page switch pager" + String.valueOf(position));
            try{
                Fragment fragment=fragments.get(position);
            }
            catch (IndexOutOfBoundsException error){
                switch (position) {
                    case 0: // Fragment # 0 - This will show FirstFragment
                        fragments.add(position, DashFragment.newInstance("0", "Page # 1"));
                    case 1:
                        fragments.add(position, ContactFragment2.newInstance(3));
                    case 2:

                        fragments.add(position, PlacesFragment.newInstance(3));
                    default:
                        fragments.add(position, DashFragment.newInstance("0", "Page # 1"));
                }
            }


            return fragments.get(position);
        }

        public Fragment getActiveFragment(ViewPager container, int position) {
            //String name = makeFragmentName(container.getId(), position);
            String name = "android:switcher:" + container.getId() + ":" + position;
            Log.i(TAG, "currentFragment" + name);
            return mFragmentManager.findFragmentByTag(name);
        }

        @Override
        public int getCount() {
            // Show 3 total pages.
            return 3;
        }

        @Override
        public CharSequence getPageTitle(int position) {
            switch (position) {
                case 0:
                    return "SECTION 1";
                case 1:
                    return "SECTION 2";
                case 2:
                    return "SECTION 3";
            }
            return null;
        }
    }


