package luan.localmotion;

public class DashItem {

    public String header;
    public String desc;
    public String aside;
    public String type;
    public String icon;
    public String color;

    public DashItem(String header, String desc, String aside, String type, String icon, String color) {
    	this.header = header;
    	this.desc = desc;
    	this.aside = aside;
    	this.type = type;
    	this.icon = icon;
        this.color = color;
    }

}
